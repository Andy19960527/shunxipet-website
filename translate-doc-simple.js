function translateText(text) {
  let result = text;
  for (const [english, chinese] of Object.entries(translationMap)) {
    const regex = new RegExp(`\\b${english}\\b`, 'gi');
    result = result.replace(regex, chinese);
  }
  return result;
}

async function main() {
  const inputPath = '/tmp/psm-report.docx';
  const outputPath = '/workspace/projects/public/psm-report-chinese.docx';
  
  console.log('Reading Word document...');
  const result = await mammoth.extractRawText({ path: inputPath });
  const text = result.value;
  
  console.log('Translating content...');
  const translatedText = translateText(text);
  
  console.log('Creating simple document structure...');
  // 创建一个简单的RTF格式的文档（可以被Word打开）
  const rtfContent = `{\\rtf1\\ansi\\ansicpg936\\deff0\\deflang1033\\deflangfe2052{\\fonttbl{\\f0\\fnil\\fcharset134 SimSun;}}
{\\colortbl;\\red0\\green0\\blue0;}
\\viewkind4\\uc1\\pard\\cf1\\lang2052\\f0\\fs24 ${translatedText.split('\n').map(line => 
    `\\par ${line.replace(/\\/g, '\\\\').replace(/{/g, '\\{').replace(/}/g, '\\}')}`
).join('')}
}`;
  
  fs.writeFileSync(outputPath, rtfContent, 'utf8');
  console.log('Translation complete! File saved to:', outputPath);
  
  // 同时保存一个纯文本版本
  const textOutputPath = '/workspace/projects/public/psm-report-chinese.txt';
  fs.writeFileSync(textOutputPath, translatedText, 'utf8');
  console.log('Text version also saved to:', textOutputPath);
}

